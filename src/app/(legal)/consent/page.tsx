import type { Metadata } from 'next';
import { LegalPage, Section, TodoOwner, Fact } from '@/components/legal';

export const metadata: Metadata = {
  title: 'Согласие на обработку персональных данных',
  description: 'Текст согласия на обработку персональных данных, которое даёт пользователь сервиса.',
};

// С 01.09.2025 (156-ФЗ, ч. 1 ст. 9 152-ФЗ) согласие оформляется ОТДЕЛЬНО:
// его нельзя объединять с офертой/условиями, а разные виды согласий — между собой.
// Это ровно то, что проверяет наш чек C5. Поэтому у согласия своя страница и свой чекбокс.

export default function ConsentPage() {
  return (
    <LegalPage
      title="Согласие на обработку персональных данных"
      intro="Текст согласия, которое пользователь даёт отдельным чекбоксом перед запуском проверки."
    >
      <Section title="Почему это отдельный документ">
        <Fact>
          С 1 сентября 2025 года согласие на обработку персональных данных оформляется отдельно от
          договора, оферты и пользовательского соглашения, а разные виды согласий не объединяются в
          одном чекбоксе. Поэтому на форме проверки согласие подтверждается самостоятельным
          непредустановленным чекбоксом.
        </Fact>
        <Fact>
          Рекламных рассылок сервис не ведёт и согласия на рекламу не собирает.
        </Fact>
      </Section>

      <Section title="Текст согласия">
        <TodoOwner>
          полный текст согласия: кому даётся (оператор, ИНН), на какие действия, с какими данными,
          с какой целью, на какой срок, порядок отзыва. Согласие не должно содержать принятие оферты
          или иных условий — только обработку персональных данных (ч. 1 ст. 9 152-ФЗ в редакции
          156-ФЗ). Формулировку утверждает юрист.
        </TodoOwner>
      </Section>

      <Section title="Как отозвать согласие">
        <TodoOwner>
          способ отзыва (email из раздела «Реквизиты»), срок реакции оператора, последствия отзыва.
        </TodoOwner>
      </Section>
    </LegalPage>
  );
}

// Маскировка персональных данных (ПС-08 §8.3, дельта правового ресёрча 07.2026).
//
// Зачем: робот обходит ЧУЖИЕ сайты, где в футере или отзывах могут лежать реальные
// ФИО/телефоны/email третьих лиц. Сохранив их в evidence или в логи, мы стали бы
// хранилищем чужих ПДн со всеми обязанностями оператора (ст. 3 152-ФЗ) и экспозицией
// по ст. 272.1 УК. Поэтому: сырой HTML живёт только в памяти, а любой текст,
// уходящий в БД или лог, проходит через maskPii().
//
// Важно: ИНН/ОГРН НЕ маскируются — это идентификаторы бизнеса, они нужны чекам B3/F1
// (сверка с реестром операторов РКН). Регулярки ниже написаны так, чтобы их не задевать.

/** `john.doe@example.com` → `j***@example.com` */
export function maskEmail(email: string): string {
  const at = email.lastIndexOf('@');
  if (at <= 0) return '***';
  const local = email.slice(0, at);
  const domain = email.slice(at + 1);
  return `${local[0]}***@${domain}`;
}

// Телефон РФ: +7/8, затем 10 цифр с произвольными разделителями.
// Границы (?<!\d) / (?!\d) не дают зацепить ИНН (10/12 цифр) и ОГРН (13/15 цифр).
const PHONE_RE = /(?<!\d)(?:\+7|8)[\s.\-()]{0,3}\d{3}[\s.\-()]{0,3}\d{3}[\s.\-]{0,2}\d{2}[\s.\-]{0,2}\d{2}(?!\d)/g;

// Локальная часть до 64 символов (RFC), домен с хотя бы одной точкой.
const EMAIL_RE = /(?<![\w.+-])([\w.+-]{1,64})@([\w-]+(?:\.[\w-]+)+)/g;

// ФИО с инициалами: «Иванов И.И.» / «И.И. Иванов» — характерный ПДн-паттерн, низкий риск ЛП.
// Полные тройные ФИО («Иван Иванович Иванов») регуляркой НЕ маскируем: эвристика съедала бы
// обычные слова с заглавной. Основная защита по ним — не хранить сырые снапшоты (ПС-08 §8.3).
const FIO_RE =
  /(?<![А-Яа-яЁё])(?:[А-ЯЁ][а-яё]+\s+[А-ЯЁ]\.\s?[А-ЯЁ]\.|[А-ЯЁ]\.\s?[А-ЯЁ]\.\s?[А-ЯЁ][а-яё]+)/g;

/**
 * Маскирует ПДн в произвольном тексте перед записью в БД или лог.
 * Применять ко всему, что взято со страницы проверяемого сайта.
 */
export function maskPii(text: string): string {
  if (!text) return text;
  return text
    .replace(EMAIL_RE, (_m, local: string, domain: string) => `${local[0]}***@${domain}`)
    .replace(PHONE_RE, '+7 *** *** ** **')
    .replace(FIO_RE, 'ФИО');
}

/** Маскировка для логов (CLAUDE.md §6: email в логи целиком не пишем). */
export function maskForLog(value: unknown): unknown {
  return typeof value === 'string' ? maskPii(value) : value;
}
